public class Adder {
    int add(int a, int b) {
        int sum = 0;
        sum = a + b;
        return sum;
    }
    double add(double a, double b) {
        double sum = 0.0;
        sum = a + b;
        return sum;
    }
    double add(double a, double b, double c) {
        double sum = 0.0;
        sum = a + b + c;
        return sum;
    }
}
